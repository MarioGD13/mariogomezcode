x = 10
y = 9
z = 3

print(f"{x} mas {y} es igual a {x+y}")
print(f"{x} menos {y} es igual a {x-y}")
print(f"{x} por {y} es igual a {x*y}")
print(f"{x} entre {y} es igual a {x/y}")

# Operador para dividir al piso: //
print(f"{y} dividido al piso de {z} es igual a {y//z}")

# Operador para sacar el resto de una divisi√≥n es %
print(f"{y} modulo de {z} es igual a {y%z}")

# Operador para elevar es **
print(f"{y} elevado a {z} es igual a {y**z}")

# Operador para raiz cuadrada es **0.5
print(f"La raiz de {y} es igual a {y**0.5}")